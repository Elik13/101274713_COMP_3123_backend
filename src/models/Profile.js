import mongoose from "mongoose";

let profileSchema = mongoose.Schema({
    id: String,
    username: {
        type: String,
        required: true
    },
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    }
});

export default mongoose.model("Profile", profileSchema);
